#include"libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	size_t	scrlen;

	scrlen = ft_strlen(src);
	if (scrlen < dstsize - 1)
	{
		ft_memcpy(dst, src, scrlen + 1);
	}
	else if (dstsize != 0)
	{
		ft_memcpy(dst, src, dstsize - 1);
		dst[dstsize - 1] = '\0';
	}
	return (scrlen);
}
